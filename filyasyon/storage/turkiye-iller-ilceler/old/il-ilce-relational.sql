  `name` varchar(64) COLLATE utf8_unicode_ci NOT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`city_plate_id`) REFERENCES city(`id`),
  UNIQUE KEY `unique_multiple_columns_1` (`city_plate_id`,`name`)
